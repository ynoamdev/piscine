void	ft_sort_integer_table(int *tab, int size)
{
	int i;
	int j;
	int stock;

	i = 0;
	while (i < (size - 1))
	{
		j = i + 1;
		while (j < size)
		{
			if (tab[i] > tab[j])
			{
				stock = tab[i];
				tab[i] = tab[j];
				tab[j] = stock;
			}
			j++;
		}
		i++;
	}
}
